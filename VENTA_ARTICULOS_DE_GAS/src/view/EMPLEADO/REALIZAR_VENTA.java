        {
            rSTextFieldIconDos8.setText("0");
        }
        DefaultTableModel modelo=(DefaultTableModel)rSTableMetro11.getModel();
        
        modelo.addRow(new Object[]{rSTextFieldIconDos4.getText(),rSTextFieldIconDos5.getText(),rSTextFieldIconDos6.getText(),rSTextFieldIconDos7.getText(),rSTextFieldIconDos8.getText(),Double.toString((Double.parseDouble(rSTextFieldIconDos7.getText())*Double.parseDouble(rSTextFieldIconDos6.getText()))-Double.parseDouble(rSTextFieldIconDos8.getText()))});
