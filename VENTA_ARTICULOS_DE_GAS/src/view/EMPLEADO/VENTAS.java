    private void rSButtonForma3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonForma3ActionPerformed
        if(rSTableMetro1.getSelectedRow()>=0)
        {
            if(JOptionPane.showConfirmDialog(null,"RECUERDA QUE SOLO PUEDES EDITAR LA CANTIDAD DE VENTA","ALERTA",JOptionPane.YES_NO_OPTION)==0)
            {
                String f=JOptionPane.showInputDialog(null, "INGRESA LA NUEVA CANTIDAD DE LA VENTA");
                if(!f.isEmpty() || Integer.parseInt(f)>0)
                {
                    ControllerVentas venta=new ControllerVentas();
                    venta.Editar_Producto(rSTableMetro1.getModel().getValueAt(rSTableMetro1.getSelectedRow(),0).toString(),f,rSTableMetro1.getModel().getValueAt(rSTableMetro1.getSelectedRow(),6).toString(),rSTableMetro1.getModel().getValueAt(rSTableMetro1.getSelectedRow(),8).toString(),rSTableMetro1.getModel().getValueAt(rSTableMetro1.getSelectedRow(),9).toString());
                    venta.Mostrar_Ventas_Emp(rSTableMetro1);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "PORFAVOR INGRESA UN DATO VALIDO");
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "PORFAVOR SELECCIONA UNA FILA");
        }// TODO add your handling code here:
    }//GEN-LAST:event_rSButtonForma3ActionPerformed


